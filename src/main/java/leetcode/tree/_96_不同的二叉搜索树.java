    class Solution{
        int[][] mem;

        public int numTrees(int n) {
            //å¤‡å¿˜å½•åˆå§‹åŒ–ä¸º0
            mem = new int[n + 1][n + 1];
            return count(1, n);
        }

        public int count(int l, int r) {
            if (l > r) {
                return 1;
            }
            //æŸ¥å¤‡å¿˜å½•
            if (mem[l][r] != 0) {
                return mem[l][r];
            }

            int res = 0, left = 0, right = 0;
            for (int i = l; i <= r; i++) {
                left = count(l, i - 1);
                right = count(i + 1, r);
                res += left * right;
            }
            mem[l][r] = res;
            return res;
        }
    }
