        public int findKthNumber(int m, int n, int k) {
            int left = 1, right = m * n;
            while (left < right) {
                int x = left + ((right - left) >> 1);
                if (getCount(m, n, x) >= k) {
                    right = x;
                }
                else {
                    left = x + 1;
                }
            }
            return left;
        }
        // å°äºxçš„ä¸ªæ•°
        private int getCount(int m, int n, int x) {
            int count = x / n * n;
            for (int i = x / n + 1; i <= m; i++) {
                count += x / i;
            }
            return count;
        }
